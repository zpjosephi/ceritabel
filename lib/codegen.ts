// R (base + dplyr)
// ---------------------------------------------------------------------------

function rAction(a: CleaningAction): string[] {
  if (a.op === "dropDuplicates") return ["df <- df[!duplicated(df), ]"];
  const c = JSON.stringify(a.column);
  switch (a.op) {
    case "dropColumn":
      return [`df[[${c}]] <- NULL`];
    case "normalizeCategory":
      return [
        `df[[${c}]] <- trimws(df[[${c}]])`,
        `.canon <- tapply(df[[${c}]], tolower(df[[${c}]]), function(v) names(sort(table(v), decreasing = TRUE))[1])`,
        `df[[${c}]] <- unname(.canon[tolower(df[[${c}]])])`,
      ];
    case "fillMissing":
      if (a.strategy === "drop") return [`df <- df[!is.na(df[[${c}]]), ]`];
      if (a.strategy === "mode")
        return [`df[[${c}]][is.na(df[[${c}]])] <- .mode(df[[${c}]])`];
      return [
        `df[[${c}]][is.na(df[[${c}]])] <- ${a.strategy}(df[[${c}]], na.rm = TRUE)`,
      ];
  }
}

export function generateR(o: CodegenOptions): string {
  const L: string[] = [];
  const usesMode = o.actions.some(
    (a) => a.op === "fillMissing" && a.strategy === "mode",
  );
  L.push("# Reproducible analysis generated by ceritabel");
  L.push(`# Dataset: ${o.fileName}`);
  if (o.isExcel) L.push("library(readxl)");
  L.push("");
  if (usesMode) {
    L.push("# helper: most frequent value (mode)");
    L.push(".mode <- function(x) { ux <- unique(x[!is.na(x)]); ux[which.max(tabulate(match(x, ux)))] }");
    L.push("");
  }
  L.push(`na <- ${rVec(NA_TOKENS)}`);
  if (o.isExcel) {
    const sheet = o.sheetName ? `, sheet = ${JSON.stringify(o.sheetName)}` : "";
    L.push(`df <- as.data.frame(read_excel(${JSON.stringify(o.fileName)}${sheet}, na = na))`);
  } else {
    L.push(`df <- read.csv(${JSON.stringify(o.fileName)}, na.strings = na, stringsAsFactors = FALSE)`);
  }
  L.push("");

  if (o.excluded.length > 0) {
    L.push("# Select variables (drop the ones you excluded)");
    L.push(`df <- df[, !(names(df) %in% ${rVec(o.excluded)})]`);
    L.push("");
  }

  if (o.actions.length > 0) {
    L.push("# Cleaning steps (same order as in the app)");
    for (const a of o.actions) L.push(...rAction(a));
    L.push("");
  }

  L.push("# Exploratory data analysis");
  L.push("str(df)");
  L.push("summary(df)");
  L.push("colSums(is.na(df))");
  L.push('cor(df[sapply(df, is.numeric)], use = "pairwise.complete.obs")  # Pearson');
  L.push("");

  L.push("# Visualizations");
  L.push("library(ggplot2)");
  L.push("num <- df[sapply(df, is.numeric)]");
  L.push("for (col in names(num)) {");
  L.push("  print(ggplot(df, aes(.data[[col]])) + geom_histogram(bins = 30) + labs(title = col))");
  L.push("}");
  L.push('if (ncol(num) >= 2) heatmap(cor(num, use = "pairwise.complete.obs"), Rowv = NA, Colv = NA, scale = "none", main = "Correlation")');
  L.push("");

  if (o.shape === "timeseries" && o.timeCol && o.valueCol) {
    const tc = JSON.stringify(o.timeCol);
    const vc = JSON.stringify(o.valueCol);
    L.push("# Time series");
    L.push(`ts <- df[order(df[[${tc}]]), c(${tc}, ${vc})]`);
    L.push(`ts <- ts[!is.na(ts[[${vc}]]), ]`);
    L.push(`ts$ma <- stats::filter(ts[[${vc}]], rep(1/${o.maWindow ?? 3}, ${o.maWindow ?? 3}), sides = 1)`);
    L.push("tail(ts)");
    L.push(`print(ggplot(ts, aes(seq_len(nrow(ts)), .data[[${vc}]])) + geom_line() + labs(title = "Time series", x = "index", y = ${vc}))`);
  } else if (o.shape === "panel" && o.entityCol && o.valueCol) {
    const ec = JSON.stringify(o.entityCol);
    const vc = JSON.stringify(o.valueCol);
    L.push("# Panel: within/between decomposition");
    L.push(`emean <- tapply(df[[${vc}]], df[[${ec}]], mean, na.rm = TRUE)`);
    L.push("between_sd <- sd(emean, na.rm = TRUE)");
    L.push(`within_sd <- sd(df[[${vc}]] - emean[as.character(df[[${ec}]])], na.rm = TRUE)`);
    L.push('cat("between SD:", between_sd, "| within SD:", within_sd, "\\n")');
    L.push(`print(ggplot(df, aes(factor(.data[[${ec}]]), .data[[${vc}]])) + geom_boxplot() + labs(x = ${ec}, y = ${vc}))`);
  }

  if (o.regTarget && o.regEntity && o.regTime) {
    const yq = JSON.stringify(o.regTarget);
    const eq = JSON.stringify(o.regEntity);
    const tq = JSON.stringify(o.regTime);
    L.push("");
    L.push("# Panel regression: pooled / fixed / random effects + Hausman");
    L.push("# needs: install.packages('plm')");
    L.push("library(plm)");
    L.push(`xs <- setdiff(names(df)[sapply(df, is.numeric)], c(${yq}, ${eq}, ${tq}))`);
    L.push(`f <- reformulate(xs, response = ${yq})`);
    L.push(`pdata <- pdata.frame(df, index = c(${eq}, ${tq}))`);
    L.push('pooled <- plm(f, pdata, model = "pooling")');
    L.push('fe <- plm(f, pdata, model = "within")   # fixed effects');
    L.push('re <- plm(f, pdata, model = "random")   # random effects');
    L.push("summary(fe); summary(re)");
    L.push("phtest(fe, re)   # Hausman test: significant -> use fixed effects");
  } else if (o.regTarget && o.regEntity) {
    const yq = JSON.stringify(o.regTarget);
    const eq = JSON.stringify(o.regEntity);
    L.push("");
    L.push(`# Fixed-effects regression (LSDV): ${o.regTarget} ~ predictors + factor(${o.regEntity})`);
    L.push(`xs <- setdiff(names(df)[sapply(df, is.numeric)], c(${yq}, ${eq}))`);
    L.push(`fit <- lm(reformulate(c(xs, sprintf("factor(\`%s\`)", ${eq})), response = ${yq}), data = df)`);
    L.push("summary(fit)  # entity fixed effects (within estimator)");
  } else if (o.regTarget) {
    const yq = JSON.stringify(o.regTarget);
    L.push("");
    L.push(`# Multiple regression (OLS): factors affecting ${o.regTarget}`);
    L.push("num <- df[sapply(df, is.numeric)]");
    L.push(`fit <- lm(num[[${yq}]] ~ ., data = num[, setdiff(names(num), ${yq})])`);
    L.push("summary(fit)  # joint F-test + partial t-tests");
    L.push("");
    L.push("# Classical assumptions  (needs: install.packages(c('lmtest','car','tseries')))");
    L.push("library(lmtest); library(car); library(tseries)");
    L.push("jarque.bera.test(resid(fit))   # normality");
    L.push("vif(fit)                        # multicollinearity");
    L.push("bptest(fit)                     # heteroscedasticity");
    L.push("dwtest(fit)                     # autocorrelation");
  }

  return L.join("\n") + "\n";
}

function rVec(arr: string[]): string {
  return `c(${arr.map((s) => JSON.stringify(s)).join(", ")})`;
}
